    try { doc.addImage(consent.nurseSignature, 'PNG', nsX + 3, sigY + 8.5, sigW - 6, 19); } catch { /**/ }
    doc.setDrawColor(SLATE3.r, SLATE3.g, SLATE3.b); doc.setLineWidth(0.3);

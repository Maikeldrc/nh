  doc.setLineWidth(0.35);
  doc.roundedRect(12, auY + 7, 186, auBodyH, 1.6, 1.6, 'FD');

  const auditRow1 = auY + 14.5;
  const auditRow2 = auY + 24.5;
  const auditRow3 = auY + 34.5;

/**
 * Utility to generate professional PDFs using jsPDF.
 */

interface PDFGeneratorMeta {
  documentId: string;
  generatedBy: string;
  dateTime: string;
}

const PAGE_WIDTH = 210;
const PAGE_HEIGHT = 297;
const CONTENT_BOTTOM = 276;

// ── Brand palette ───────────────────────────────────────────────────────────
const TEAL   = { r: 10,  g: 148, b: 155 }; // primary teal #0A949B
const NAVY   = { r: 15,  g: 45,  b: 90  }; // dark navy  #0F2D5A
const SLATE6 = { r: 71,  g: 85,  b: 105 }; // slate-600
const SLATE5 = { r: 100, g: 116, b: 139 }; // slate-500
const SLATE3 = { r: 203, g: 213, b: 225 }; // slate-300
const INK    = { r: 15,  g: 23,  b: 42  }; // slate-900

function setTeal(doc: jsPDF)  { doc.setTextColor(TEAL.r, TEAL.g, TEAL.b); }
function setNavy(doc: jsPDF)  { doc.setTextColor(NAVY.r, NAVY.g, NAVY.b); }
function setInk(doc: jsPDF)   { doc.setTextColor(INK.r,  INK.g,  INK.b);  }
function setSlate6(doc: jsPDF){ doc.setTextColor(SLATE6.r, SLATE6.g, SLATE6.b); }
function setSlate5(doc: jsPDF){ doc.setTextColor(SLATE5.r, SLATE5.g, SLATE5.b); }

function applyPdfMetadata(doc: jsPDF, documentTitle: string): void {
  doc.setProperties({
    title: `${PRODUCT_NAME} - ${documentTitle}`,
    subject: `${documentTitle} generated by ${PRODUCT_NAME}`,
    author: PRODUCT_NAME,
    creator: `${PRODUCT_NAME} | ${POWERED_BY}`
  });
}

function drawWrappedText(doc: jsPDF, text: string, x: number, y: number, maxWidth: number, lineHeight = 4): number {
  const lines = doc.splitTextToSize(text, maxWidth);
  doc.text(lines, x, y);
  return y + (lines.length * lineHeight);
}

function drawFitText(
  doc: jsPDF,
  text: string,
  x: number,
  y: number,
  maxWidth: number,
  fontSize: number,
  minFontSize = 5.8
): void {
  let size = fontSize;
  doc.setFontSize(size);
  while (doc.getTextWidth(text) > maxWidth && size > minFontSize) {
    size -= 0.2;
    doc.setFontSize(size);
  }
  if (doc.getTextWidth(text) <= maxWidth) {
    doc.text(text, x, y);
    doc.setFontSize(fontSize);
    return;
  }

  const ellipsis = '...';
  let clipped = text;
  while (clipped.length > 0 && doc.getTextWidth(`${clipped}${ellipsis}`) > maxWidth) {
    clipped = clipped.slice(0, -1);
  }
  doc.text(`${clipped}${ellipsis}`, x, y);
  doc.setFontSize(fontSize);
}

/** Teal bullet used in consent terms */
function drawBullet(doc: jsPDF, text: string, x: number, y: number, maxWidth: number): number {
  doc.setFillColor(TEAL.r, TEAL.g, TEAL.b);
  doc.circle(x + 0.8, y - 1.1, 0.65, 'F');
  return drawWrappedText(doc, text, x + 3.4, y, maxWidth - 3.4, 3.6);
}

/** Section heading card: left teal accent bar + section number + label */
function drawSectionHeader(doc: jsPDF, label: string, y: number): void {
  // left teal bar
  doc.setFillColor(TEAL.r, TEAL.g, TEAL.b);
  doc.rect(12, y, 2, 5.6, 'F');
  // text
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(8.2);
  setTeal(doc);
  doc.text(label.toUpperCase(), 16.5, y + 4.2);
}

/** Draws a key-value pair: label in slate-500, value in bold ink */
function drawKV(doc: jsPDF, label: string, value: string, lx: number, vx: number, y: number): void {
  doc.setFont('helvetica', 'normal'); setSlate5(doc); doc.setFontSize(8);
  doc.text(label, lx, y);
  doc.setFont('helvetica', 'bold'); setInk(doc); doc.setFontSize(8);
  doc.text(value, vx, y);
}

function drawStackedKV(
  doc: jsPDF,
  label: string,
  value: string,
  x: number,
  y: number,
  maxWidth: number,
  valueFontSize = 7.1,
  minFontSize = 5.8
): void {
  doc.setFont('helvetica', 'normal');
  doc.setFontSize(5.7);
  setSlate5(doc);
  doc.text(label, x, y);
  doc.setFont('helvetica', 'bold');
  setInk(doc);
  drawFitText(doc, value, x, y + 3.8, maxWidth, valueFontSize, minFontSize);
}

/** Rounded badge pill */
function drawBadge(doc: jsPDF, label: string, x: number, y: number, bg: number[], fg: number[] = [255,255,255]): void {
  const w = doc.getStringUnitWidth(label) * 7.5 / doc.internal.scaleFactor + 4;
  doc.setFillColor(bg[0], bg[1], bg[2]);
  doc.roundedRect(x, y - 3.5, w, 5, 1.5, 1.5, 'F');
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(6.5);
  doc.setTextColor(fg[0], fg[1], fg[2]);
  doc.text(label, x + 2, y);
}

function drawHeader(doc: jsPDF, _title: string, patient: Patient, meta: PDFGeneratorMeta) {
  // ── Top white band ──────────────────────────────────────────────────────
  doc.setFillColor(255, 255, 255);
  doc.rect(0, 0, PAGE_WIDTH, 24, 'F');

  // Decorative teal circle top-right, fully inside the 7-10 mm safe area.
  doc.setFillColor(TEAL.r, TEAL.g, TEAL.b);
  doc.circle(194, 11.5, 5.8, 'F');
  // ECG / heartbeat line inside the circle (simplified polyline)
  doc.setDrawColor(255, 255, 255);
  doc.setLineWidth(0.55);
  doc.lines([[1.2,0],[0.8,-1.8],[0.8,3.4],[0.8,-2.7],[0.8,1.4],[1.2,0]], 190.2, 11.5, [1,1]);

  // AMAVITA logo
  try {
    doc.addImage(amavitaLogo, 'PNG', 12, 4.5, 52, 8, 'amavita-logo', 'FAST');
  } catch { /* silent */ }

  // Product name and powered-by line.
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(7);
  setNavy(doc);
  doc.text(PRODUCT_NAME, 13.5, 16.5);
  doc.setFontSize(5.4);
  setTeal(doc);
  doc.text(POWERED_BY, 13.5, 20.2);

  // Doc meta (right column)
  const metaX = 125;
  const labelW = 12;
  doc.setFontSize(6.4);

  setSlate6(doc); doc.setFont('helvetica', 'normal');
  doc.text('DOC ID:', metaX, 8.2);
  doc.text('DATE:',   metaX, 12.6);
  doc.text('BY:',     metaX, 17);

  setNavy(doc); doc.setFont('helvetica', 'bold');
  drawFitText(doc, meta.documentId, metaX + labelW, 8.2, 45, 6.4);
  drawFitText(doc, meta.dateTime, metaX + labelW, 12.6, 45, 6.4);
  drawFitText(doc, meta.generatedBy, metaX + labelW, 17, 45, 6.4);

  // Thin separator
  doc.setDrawColor(SLATE3.r, SLATE3.g, SLATE3.b);
  doc.setLineWidth(0.3);
  doc.line(0, 24, PAGE_WIDTH, 24);
}

function drawFooter(doc: jsPDF, meta: PDFGeneratorMeta, pageNum: number) {
  const fY = PAGE_HEIGHT - 13;

  // Navy band
  doc.setFillColor(NAVY.r, NAVY.g, NAVY.b);
  doc.rect(0, fY, PAGE_WIDTH, 13, 'F');

  // Teal accent stripe (left edge only)
  doc.setFillColor(TEAL.r, TEAL.g, TEAL.b);
  doc.rect(0, fY, 3, 13, 'F');

  // Main footer text (starts right after the stripe, no icon circle)
  doc.setFont('helvetica', 'bold'); doc.setFontSize(6.3); doc.setTextColor(255, 255, 255);
  doc.text(`${PRODUCT_NAME.toUpperCase()}  \u2022  ${POWERED_BY.toUpperCase()}  \u2022  CONFIDENTIAL MEDICAL RECORD  \u2022  HIPAA COMPLIANT`, 7, fY + 5.5);
  // Doc ID line — white
  doc.setFont('helvetica', 'normal'); doc.setFontSize(5.8); doc.setTextColor(255, 255, 255);
  doc.text(`Document Unique ID: ${meta.documentId}  \u2022  Page ${pageNum}`, 7, fY + 10.2);

  // Decorative ECG at right
  doc.setDrawColor(TEAL.r, TEAL.g, TEAL.b);
  doc.setLineWidth(0.65);
  doc.lines([[4,0],[1.5,-3],[1.5,6],[1.5,-4.5],[1.5,2.2],[4,0]], 177, fY + 7, [1,1]);
}

function drawAllFooters(doc: jsPDF, meta: PDFGeneratorMeta) {
  const pageCount = doc.getNumberOfPages();
  for (let page = 1; page <= pageCount; page += 1) {
    doc.setPage(page);
    drawFooter(doc, meta, page);
  }
}

function ensureSectionSpace(
  doc: jsPDF,
  requiredHeight: number,
  currentY: number,
  title: string,
  patient: Patient,
  meta: PDFGeneratorMeta
): number {
  if (currentY + requiredHeight <= CONTENT_BOTTOM) return currentY;
  doc.addPage();
  drawHeader(doc, title, patient, meta);
  return 36;
}

